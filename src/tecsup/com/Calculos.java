package tecsup.com;

public class Calculos {
	
	
	private static int resultado;
	
	public static int metodosuma(int num1,int num2){
		resultado=num1+num2;
		return resultado;
	}
	public int metodoresta(int num1,int num2){
		resultado=num1-num2;
		return resultado;
	}

	public int metodoproducto(int num1,int num2){
		resultado=num1*num2;
		return resultado;
	}
	
	
}
